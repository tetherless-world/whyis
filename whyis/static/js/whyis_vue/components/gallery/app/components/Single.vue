<template>
  <div class="">
    <div class="utility-content__result">Home > Single Chart > <span class="utility-color"><strong>{{ args.label }}</strong></span></div>
    <div class="loading-dialog" v-if="loading">
      <spinner :loading="loading" text='loading data...'/>
    </div>
    <div class="loading-dialog" v-else>
      <div class="h6">{{args.label}}</div>
      <vega-lite :spec="spec" />
      <div class="utility-margin"><span><strong>Description:</strong></span>{{ args.description}}</div>
    </div>
    <!-- Bootstrap Speed Dial Replacement -->
    <div class="position-fixed bottom-0 end-0 p-3">
      <div class="d-flex flex-column gap-2">
        <button class="btn btn-primary rounded-circle p-2 utility-float-icon" @click.prevent="navBack" aria-label="Go Back" style="width: 48px; height: 48px;">
          <i class="bi bi-arrow-left"></i>
        </button>
        <button class="btn btn-secondary rounded-circle p-2" aria-label="Share" style="width: 48px; height: 48px;">
          <i class="bi bi-share"></i>
        </button>
      </div>
    </div>
  </div>
</template>
<style lang="scss" src="../../../../assets/css/main.scss"></style>
<script>
  import EventServices from '../../../../modules/events/event-services'
  import { loadChart, buildSparqlSpec } from '../../../../utilities/vega-chart'
  import { querySparql } from '../../../../utilities/sparql'

  export default {
    name: 'single',
    props:{
      globalargs: {
        type: String
      }
    },
    data() {
      return {
        filter: false,
        bottomPosition:'bottom-0 end-0',
        loading: true,
        spec: null,
        chart: null,
        authenticated: false,
        speedDials: EventServices.speedDials,
      }
    },
    methods: {
      loadVisualization () {
        this.loading = true;
        loadChart(this.args.identifier)
          .then(chart => {
            this.chart = chart
            return querySparql(chart.query)
          })
          .then(sparqlResults => {
            this.spec = buildSparqlSpec(this.chart.baseSpec, sparqlResults)
          })
          .finally(() => this.loading = false)
      },
      navBack(){
        return this.goBack()
      }
    },
    beforeMount(){
      return this.loadVisualization()
    },
    created () {
      
    }
  }
</script>